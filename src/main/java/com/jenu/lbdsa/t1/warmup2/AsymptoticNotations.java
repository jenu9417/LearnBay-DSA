public class AsymptoticNotations {
	
/*

Big-Oh
------

Big-Oh notation is used to define the upper bound for a function,
which will hold true for all the variables greater than n0.

	O(g(n)) = { f(n): there exist positive constants c and 
	                  n0 such that 0 <= f(n) <= c*g(n) for 
	                  all n >= n0}

	
Omega
------

Omega notation is used to define the lower bound for a function,
which will hold true for all the variables greater than n0.

	Ω (g(n)) = {f(n): there exist positive constants c and
                  n0 such that 0 <= c*g(n) <= f(n) for
                  all n >= n0}.

	
Theta
-----

Omega notation is used to define both upper and lower bound for a 
function, which will hold true for all the variables greater than n0.

	Θ(g(n)) = {f(n): there exist positive constants c1, c2 and n0 such 
                 that 0 <= c1*g(n) <= f(n) <= c2*g(n) for all n >= n0}
                 
 */

}
